export function getTheme(mode: ThemeMode, systemPrefersDark: boolean): Theme {
  if (mode === 'system') {
    return systemPrefersDark ? darkTheme : lightTheme;
  }
  return mode === 'dark' ? darkTheme : lightTheme;
}

